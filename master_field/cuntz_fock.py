def build_number_operator(fock: CuntzFockSpace, matrix_idx: int = 0) -> np.ndarray:
    """N̂ = Σ_{k=1}^L (a†)^k a^k

    In the Cuntz algebra this is NOT simply a†a.
    It counts the 'position' of a particle in the word.
    """
    a = fock.a(matrix_idx)
    adag = fock.adag(matrix_idx)
    N_hat = np.zeros((fock.dim, fock.dim))
    adag_k = adag.copy()
    a_k = a.copy()
    for _k in range(1, fock.L + 1):
        N_hat += adag_k @ a_k
        adag_k = adag_k @ adag
        a_k = a_k @ a
    return N_hat


